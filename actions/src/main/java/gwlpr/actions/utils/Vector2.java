package gwlpr.actions.utils;


/**
 * A 2 dimensional vector.
 * 
 * @author _rusty
 */
public class Vector2 
{
    
    private final float x,y;

    
    public Vector2(float x, float y) 
    {
        this.x = x;
        this.y = y;
    }

    
    public float getX() 
    {
        return x;
    }

    
    public float getY() 
    {
        return y;
    }
}
