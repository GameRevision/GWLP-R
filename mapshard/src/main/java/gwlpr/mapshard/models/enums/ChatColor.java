package gwlpr.mapshard.models.enums;


/**
 * This Enum represents the available chat colors for ingame chat.
 * 
 * Note that is usually a combination of two different colors, as in
 * (CharName - Color 1) (ActualText - Color 2)
 * 
 * @author _rusty
 */
public enum ChatColor 
{
    // Alliance Chat
    Orange_White,

    // Allied Party Chat
    BlueAlly_White,

    // Broadcasts
    LightBlue_LightBlue,

    // All Chat
    Yellow_White,

    // No Official Usage
    Gray_DarkGray,

    // Gaile's Chat / turquoise
    Turquoise_Turquoise,

    // Emotes
    White_White,

    // No Official Usage
    Invisible_Invisible,

    // The Frog's Chat / turquoise
    Gray_Turquoise,

    // Guild Chat
    Green_White,

    // Announcements
    LightGreen_LightGreen,

    // Team Chat
    BlueTeam_White,

    // Trade Chat
    LightPink_LightPink,

    // Command reply
    DarkOrange_DarkOrange,

    // NPC's Chat
    BlueNPC_White
}
